CREATE VIEW [OXO_Programme_Rule_VW]
AS
SELECT     TOP (100) PERCENT PR.Id, PR.Programme_Id, 
                      CASE PR.Rule_Category WHEN 'E' THEN 'Engineering' WHEN 'M' THEN 'Marketing' WHEN 'T' THEN 'Territorial' END AS Rule_Category, 
                      PR.Rule_Group AS Rule_Group_Id, ISNULL(RL.Description, 'Unspecified') AS Rule_Group, PR.Rule_Assert, PR.Rule_Report, PR.Rule_Response, 
                      PR.Rule_Reason, PR.Owner, PR.Active, 
                      PR.Created_By, PR.Created_On, PR.Updated_By, PR.Last_Updated
FROM         dbo.OXO_Programme_Rule AS PR LEFT OUTER JOIN
             dbo.OXO_Reference_List AS RL ON PR.Rule_Group = RL.Code
AND          RL.List_Name = 'Feature Group'
ORDER BY PR.Programme_Id, Rule_Category, Rule_Group


